a ='Weight of the conductor =%8.3f kg/m'; 
set(handles.edit6,'string',W)
Wt = sqrt(W^2+Ww^2);    %Total weight
set(handles.edit7,'string',Wt)
Sag = (Wt*(l^2))/(8*T); x = Ww/W; 
set(handles.edit8,'string',Sag);
Theta = atan(x);
set(handles.edit9,'string',Theta);
VSag = Sag*cos(Theta); %Vertical Sag
set(handles.edit10,'string',VSag)

