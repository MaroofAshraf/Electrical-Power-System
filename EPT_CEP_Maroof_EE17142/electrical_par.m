% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
L=str2double(get(handles.edit3,'string'));
Vl=str2double(get(handles.edit1,'string'));
r=str2double(get(handles.edit4,'string'));
l=str2double(get(handles.edit5,'string'));
S=str2double(get(handles.edit2,'string'));
Fi=str2double(get(handles.edit6,'string'));
S=S*(10^6);
Vl=Vl*(10^3);
l=l*(10^-3);
ind=L*(l);
R=r*(L);
Vr=Vl/sqrt(3);
Z=complex(R,2*pi*50*ind);
j=sqrt(-1);
if L <= 60
A=1;
B=Z;
C=0;
D=A;
elseif L > 60 && L <= 160
c=str2double(get(handles.edit7,'string'));
c=c*(10^-6);
Y=2*pi*50*c*L*j;
A=(Y/2)*Z+1;
B=Z*((Y/4)*Z+1);
C=Y;
D=A;
else
c=str2double(get(handles.edit7,'string'));
c=c*(10^-6);
Y=2*pi*50*c*L*j;
K=sqrt(Y*Z);
M=sqrt(Y/Z);
A=cosh(K);
B=sinh(K)/M;
C=M*sinh(K);
D=A;
end
Ir=S/((sqrt(3)*Vl));
IR =((Ir))*complex(cos(-acos(Fi)),sin(-acos(Fi)));
VS=A*Vr+B*IR;
IS=C*Vr+D*IR;
Ps=3*real(VS*(conj(IS)));
VR=abs((((abs(VS)/abs(A))-abs(Vr))/abs(Vr)))*100;
Pr=S*0.8;
EF=(Pr/Ps)*100;
Qs=3*imag(VS*(conj(IS)));
F=cos(atan(Qs/Ps));
set(handles.edit9,'string',Vr)
set(handles.edit10,'string',IS)
set(handles.edit11,'string',F)
set(handles.edit12,'string',VR)
set(handles.edit13,'string',EF)



